
public class AssignmentOperator {

	public static void main(String[] args) {
		int a = 5;
		int b = 3;
		int c = 4;
		System.out.println("a = "+a);
		a += b; // a = a+b;
		System.out.println("a+b="+a);
		a -= b; // a = a-b;
		System.out.println("a-b="+a);
		a *= b; // a = a*b;
		System.out.println("a*b="+a);
		a /= c ;// a = a/c;
		System.out.println("a/c="+a);
		a %= c; // a = a%c ;
		System.out.println("a%c="+a);
		System.out.println("A"+"B");
	}

}
